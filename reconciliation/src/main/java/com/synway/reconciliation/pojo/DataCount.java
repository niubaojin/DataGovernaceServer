package com.synway.reconciliation.pojo;

/**
 * 数据量
 * @author ym
 */
public class DataCount {
    private int accessStorageCount;
    private int standardCount;
    private int accessAccessCount;

    public int getAccessStorageCount() {
        return accessStorageCount;
    }

    public void setAccessStorageCount(int accessStorageCount) {
        this.accessStorageCount = accessStorageCount;
    }

    public int getStandardCount() {
        return standardCount;
    }

    public void setStandardCount(int standardCount) {
        this.standardCount = standardCount;
    }

    public int getAccessAccessCount() {
        return accessAccessCount;
    }

    public void setAccessAccessCount(int accessAccessCount) {
        this.accessAccessCount = accessAccessCount;
    }
}
