package com.synway.governace.pojo;

public class NavigationBar {
    private int navigationId;
    private String navigationName;
    private String navigationCode;
    private String navigationLevel;
    private String navigationParentId;
    private String navigationParentName;
    private String navigationClass;
    private String navigationUrl;
    private Integer navigationOrder;
    private String navigationChildId;
    private String navigationChildStyle;

    public int getNavigationId() {
        return navigationId;
    }

    public void setNavigationId(int navigationId) {
        this.navigationId = navigationId;
    }

    public String getNavigationName() {
        return navigationName;
    }

    public void setNavigationName(String navigationName) {
        this.navigationName = navigationName;
    }

    public String getNavigationCode() {
        return navigationCode;
    }

    public void setNavigationCode(String navigationCode) {
        this.navigationCode = navigationCode;
    }

    public String getNavigationLevel() {
        return navigationLevel;
    }

    public void setNavigationLevel(String navigationLevel) {
        this.navigationLevel = navigationLevel;
    }

    public String getNavigationParentId() {
        return navigationParentId;
    }

    public void setNavigationParentId(String navigationParentId) {
        this.navigationParentId = navigationParentId;
    }

    public String getNavigationParentName() {
        return navigationParentName;
    }

    public void setNavigationParentName(String navigationParentName) {
        this.navigationParentName = navigationParentName;
    }

    public String getNavigationClass() {
        return navigationClass;
    }

    public void setNavigationClass(String navigationClass) {
        this.navigationClass = navigationClass;
    }

    public String getNavigationUrl() {
        return navigationUrl;
    }

    public void setNavigationUrl(String navigationUrl) {
        this.navigationUrl = navigationUrl;
    }

    public Integer getNavigationOrder() {
        return navigationOrder;
    }

    public void setNavigationOrder(Integer navigationOrder) {
        this.navigationOrder = navigationOrder;
    }

    public String getNavigationChildId() {
        return navigationChildId;
    }

    public void setNavigationChildId(String navigationChildId) {
        this.navigationChildId = navigationChildId;
    }

    public String getNavigationChildStyle() {
        return navigationChildStyle;
    }

    public void setNavigationChildStyle(String navigationChildStyle) {
        this.navigationChildStyle = navigationChildStyle;
    }
}
