-- Create table 任务状态表
CREATE TABLE SHPTSTATISTICS.SJGC_DATA_TASK (
  SERIAL_NUMBER     VARCHAR(64)  COMMENT '记录序列号',
  TASK_ID           VARCHAR(64)  COMMENT '任务ID',
  INSTANCE_ID       VARCHAR(64)  COMMENT '实例ID',
  TASK_NAME         VARCHAR(256) COMMENT '任务名称',
  TASK_CLASS        VARCHAR(256) COMMENT '大类',
  TASK_SUBCLASS     VARCHAR(256) COMMENT '小类',
  IN_SOURCE_NAME    VARCHAR(256) COMMENT '输入数据源名称',
  IN_CN_NAME        VARCHAR(256) COMMENT '输入数据名称',
  IN_PROTOCOL       VARCHAR(256) COMMENT '输入数据协议名',
  OUT_SOURCE_NAME   VARCHAR(256) COMMENT '输出数据源名称',
  TASK_STATUS       VARCHAR(64)  COMMENT '任务状态',
  NODE_IP           VARCHAR(64)  COMMENT '节点IP',
  NODE_PORT         VARCHAR(64)  COMMENT '节点端口',
  SOURCE_FILETYPE   VARCHAR(256) COMMENT '来源文件类型',
  TASK_START_TIME   DATETIME     COMMENT '任务开始时间',
  TASK_END_TIME     DATETIME     COMMENT '任务结束时间',
  TASK_RUNNING_TIME BIGINT       COMMENT '任务运行时长',
  CREATE_TIME       DATETIME     COMMENT '入表时间'
) COMMENT = '任务状态表';

-- Create table 接入处理入库数据汇总表
CREATE TABLE SHPTSTATISTICS.SJGC_DATA_SUMMARY (
  SERIAL_NUMBER   VARCHAR(64)  COMMENT '记录序列号',
  INSTANCE_ID     VARCHAR(64)  COMMENT '实例ID',
  DATA_TYPE       VARCHAR(64)  COMMENT '标识',
  NODE_IP         VARCHAR(64)  COMMENT '节点IP',
  NODE_PORT       VARCHAR(64)  COMMENT '节点端口',
  IN_CN_NAME      VARCHAR(256) COMMENT '输入数据名称',
  IN_PROTOCOL     VARCHAR(256) COMMENT '输入数据协议名',
  IN_SUCCESS_NUM  BIGINT       COMMENT '输入数据条数',
  IN_SUCCESS_SIZE BIGINT       COMMENT '输入数据大小',
  OUT_CN_NAME     VARCHAR(256) COMMENT '输出数据名称',
  OUT_EN_NAME     VARCHAR(256) COMMENT '输出数据英文表名',
  OUT_PROTOCOL    VARCHAR(256) COMMENT '输出数据协议名',
  OUT_SUCCESS_NUM BIGINT       COMMENT '输出成功条数',
  OUT_ERROR_NUM   BIGINT       COMMENT '输出异常条数',
  OUT_BIDDING_NUM BIGINT       COMMENT '输出中标条数',
  STORE_TYPE      VARCHAR(256) COMMENT '输出平台',
  PROJECT_NAME    VARCHAR(256) COMMENT '输出平台项目空间',
  PUSH_DATE       DATETIME     COMMENT '推送日期',
  PUSH_HOUR       INT          COMMENT '数据时点',
  CREATE_TIME     DATETIME     COMMENT '入表时间'
) COMMENT = '接入处理入库数据汇总表';
